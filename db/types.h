#define DTTZ_TEXT_TO_PREC(str, prec, def, action)                              \
    if (str == NULL) {                                                         \
        action;                                                                \
    } else if ((str)[0] == 'm' || (str)[0] == 'M')                             \
        prec = DTTZ_PREC_MSEC;                                                 \
    else if ((str)[0] == 'u' || (str)[0] == 'U')                               \
        prec = DTTZ_PREC_USEC;                                                 \
    else if (atoi(str) == DTTZ_PREC_MSEC)                                      \
        prec = DTTZ_PREC_MSEC;                                                 \
    else if (atoi(str) == DTTZ_PREC_USEC)                                      \
        prec = DTTZ_PREC_USEC;                                                 \
    else if (def)                                                              \
        prec = gbl_datetime_precision;                                         \
    else {                                                                     \
        action;                                                                \
    }

